package operators;

public class UnaryOperator {

	public static void main(String[] args) 
	{
		int a = 10;
		// incremenet operator
		a++;
		System.out.println("incremented value");
		System.out.println(a);
		
		
		int b = 10;
		// decrement operator
		b--;
		System.out.println("decremented value");
		System.out.println(b);
	}

}
